package application;

import domain.Lotto;
import domain.MatchResult;
import domain.WinningLotto;
import view.LottoGuidePrinter;
import view.LottoScanner;

import java.util.List;

public class LottoManager {

    public static void run() {
        int purchaseQuantity = inputPurchaseQuantity();
        List<Lotto> autoGeneratedLotto = purchaseAutoGeneratedLotto(purchaseQuantity);
        WinningLotto winningLotto = inputWinnningLotto();
        MatchResult matchResult = new LottoResultManager(winningLotto, autoGeneratedLotto).getResult();
        LottoGuidePrinter.printLottoResult(purchaseQuantity * Lotto.PRICE, matchResult);
    }

    private static int inputPurchaseQuantity() {
        int purchaseQuantity = LottoScanner.getPurchaseQuantity();
        LottoGuidePrinter.alertPurchaseQuantity(purchaseQuantity);
        return purchaseQuantity;
    }

    private static List<Lotto> purchaseAutoGeneratedLotto(int purchaseQuantity) {
        AutoGeneratedLottoPurchaser lottoPurchaser = new AutoGeneratedLottoPurchaser();
        List<Lotto> lottoList = lottoPurchaser.generateLotto(purchaseQuantity);
        LottoGuidePrinter.printLottoList(lottoList);
        return lottoList;
    }

    private static WinningLotto inputWinnningLotto() {
        WinningLottoGenerator lottoGenerator = new WinningLottoGenerator();
        return (WinningLotto) lottoGenerator.getLotto();
    }
}
