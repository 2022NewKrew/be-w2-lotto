package domain;

import validator.LottoValidator;

import java.util.List;

public class Purchase {
    public static final int ONE_LOTTO_PRICE = 1000;
    private final int amount;
    private final List<Lotto> autoGeneratedLotto;

    public Purchase(int amount, List<Lotto> autoGeneratedLotto) {
        LottoValidator.assertValidPurchaseAmount(amount);
        this.amount = amount;
        this.autoGeneratedLotto = autoGeneratedLotto;
    }

    public int getAmount() {
        return amount;
    }

    public static int purchaseableQuantity(int amount) {
        if ((amount < 0) || (amount % ONE_LOTTO_PRICE != 0)) {
            return 0;
        }
        return amount / ONE_LOTTO_PRICE;
    }

    public int getTotalPurchaseQuantity() {
        return amount / ONE_LOTTO_PRICE;
    }

    public List<Lotto> getAutoGeneratedLotto() {
        return autoGeneratedLotto;
    }
}
